# ESERCIZIO: Tupla colori

# Crea tupla con 3 colori
colori = ("rosso", "blu", "rosso")
print("Tupla colori:", colori)

# Stampa primo e ultimo
print("Primo colore:", colori[0])
print("Ultimo colore:", colori[-1])

# Conta quante volte compare "rosso"
conteggio = colori.count("rosso")
print("Il colore 'rosso' compare", conteggio, "volte")
